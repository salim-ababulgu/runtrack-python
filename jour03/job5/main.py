# Créer une fonction nommée « calcule » avec 3 paramètres
    # --> Le premier, « num1 », est un nombre
    # --> Le deuxième, « operator », est un caractère 
        # (string) contenant le type d’opération(+, -, *, /, %)
    # --> Le troisième, « num2 », est un nombre
# La fonction doit retourner le résultat de l’opération

def calcule(num1, num2):
    return num1 + num2

resultat = calcule(10, 20)
print(resultat)

