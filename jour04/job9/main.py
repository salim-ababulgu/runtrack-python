# Écrire un programme qui récupère la valeur,
# maximum et le minimum des éléments de la liste 
# : L = [8, 24, 27, 48, 2,16, 9, 102, 7, 84, 91]
# Résultat attendu : 
#   la valeur max est: 102
#   la valeur min est: 2



numbers = [8, 24, 27, 48, 2,16, 9, 102, 7, 84, 91]
# Trouver la valeur maximum grace au méthode 'max()'
max_number = max(numbers)
print(max_number)

# Trouver la valeur minimum grace au méthode 'min()'
min_number = min(numbers)
print(min_number)





















