# Afficher l'alphabet à l'envers dans le terminal sans utiliser def
alphabet = "abcdefghijklmnopqrstuvwxyz" # Je déclare la variable "alphabet"
alphabet_renverse = alphabet[::-1] # Je déclare une nouvelle variable pour modifier le variable 'alphabet' et j'utilise [::-1] pour inverser la chaîne de caractères
print("L'alphabet à l'envers : ", alphabet_renverse) # J'imprime le résultat de ma nouvelle variable
